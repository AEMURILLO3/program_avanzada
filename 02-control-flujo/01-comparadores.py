# Comparaciones en Python

print(1 > 2)   # Compara si 1 es mayor que 2. Resultado: False
print(1 < 2)   # Compara si 1 es menor que 2. Resultado: True

print(1 <= 2)  # Compara si 1 es menor o igual que 2. Resultado: True
print(1 >= 2)  # Compara si 1 es mayor o igual que 2. Resultado: False

print(1 == 2)  # Compara si 1 es igual a 2. Resultado: False
# Compara si 1 es igual a "1" (comparaci√≥n entre entero y string). Resultado: False
print(1 == "1")

print(1 != 2)  # Compara si 1 es diferente de 2. Resultado: True
