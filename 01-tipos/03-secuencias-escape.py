# Esto es un comentario
# \"
# \'
# \\
# \n
# \t

# Ejemplo 1: Uso de comillas dentro de una cadena
name_with_quotes = "\"Jeanne\""
print("Ejemplo 1:", name_with_quotes)  # Imprime: "Jeanne"

# Ejemplo 2: Uso de comillas simples dentro de una cadena
name_with_single_quotes = '\'Jeanne\''
print("Ejemplo 2:", name_with_single_quotes)  # Imprime: 'Jeanne'

# Ejemplo 3: Incluir una barra invertida en una cadena
path = "C:\\Users\\Jorge\\Documents"
print("Ejemplo 3:", path)  # Imprime: C:\Users\Jorge\Documents

# Ejemplo 4: Usar un salto de línea en una cadena
text_with_newline = "Primera línea\nSegunda línea"  # \n para un salto de línea
print("Ejemplo 4:")
print(text_with_newline)
# Imprime:
# Primera línea
# Segunda línea

# Ejemplo 5: Usar tabulación en una cadena
text_with_tab = "Nombre:\tJeanne\tEdad:\t25"  # \t para tabulaciones
print("Ejemplo 5:", text_with_tab)  # Imprime: Nombre:	Jeanne	Edad:	25

# Ejemplo 6: Combinación de caracteres especiales
combined_example = "Este es el primer párrafo.\n\tEste es un párrafo con tabulación."
print("Ejemplo 6:")
print(combined_example)
# Imprime:
# Este es el primer párrafo.
# 	Este es un párrafo con tabulación.
